// --- Main ---

function main() {
  const allEntries = [
    ...scanHooks(),
    ...scanDefaultJobs(),
    ...scanSkills(),
    ...scanRouteGroups(),
    ...scanCliCommands(),
    ...scanTemplates(),
    ...scanSubsystems(),
  ];

  // Build the manifest
  const manifest = {
    $schema: './builtin-manifest.schema.json',
    schemaVersion: 1,
    generatedAt: new Date().toISOString(),
    instarVersion: require(path.join(ROOT, 'package.json')).version,
    entryCount: allEntries.length,
    entries: {},
  };

  for (const entry of allEntries) {
    manifest.entries[entry.id] = entry;
  }

  // Write to src/data/
  const outputPath = path.join(ROOT, 'src/data/builtin-manifest.json');
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, JSON.stringify(manifest, null, 2) + '\n');

  // Summary
  const byType = {};
  for (const entry of allEntries) {
    byType[entry.type] = (byType[entry.type] || 0) + 1;
  }

  console.log(`Generated builtin-manifest.json: ${allEntries.length} entries`);
  for (const [type, count] of Object.entries(byType).sort()) {
    console.log(`  ${type}: ${count}`);
  }
}

main();
