#!/usr/bin/env node
/**
 * generate-builtin-manifest.js
 *
 * Scans the Instar source tree to enumerate all built-in capabilities and
 * produces src/data/builtin-manifest.json with content hashes.
 *
 * Run at build time: node scripts/generate-builtin-manifest.js
 * CI validation: npm test includes a test that verifies the manifest is up-to-date.
 */

const fs = require('fs');
const path = require('path');
const crypto = require('crypto');
const { execSync } = require('child_process');

const ROOT = path.resolve(__dirname, '..');

// Guard: warn if working tree is dirty (manifest won't match committed source in CI)
try {
  const status = execSync('git status --porcelain -- src/', { cwd: ROOT, encoding: 'utf-8' }).trim();
  if (status) {
    console.warn('\n⚠️  WARNING: Working tree has uncommitted changes in src/.');
    console.warn('   The generated manifest may not match committed source.');
    console.warn('   CI test "is up-to-date with current source" will fail if you commit this.');
    console.warn('   Stash WIP first: git stash push -u\n');
  }
} catch { /* not a git repo or git not available — skip check */ }

function sha256(content) {
  return crypto.createHash('sha256').update(content).digest('hex');
}

function sha256File(filePath) {
  const content = fs.readFileSync(filePath, 'utf-8');
  return sha256(content);
}

function fileExists(filePath) {
  return fs.existsSync(filePath);
}

/**
 * Scan hooks from PostUpdateMigrator's migrateHooks method.
 * These are the 13 hook files Instar writes to .instar/hooks/instar/.
 */
function scanHooks() {
  const entries = [];

  // The canonical list of built-in hooks, matching PostUpdateMigrator.migrateHookLayout's builtinHooks
  const builtinHooks = [
    { file: 'session-start.sh', domain: 'identity' },
    { file: 'dangerous-command-guard.sh', domain: 'safety' },
    { file: 'grounding-before-messaging.sh', domain: 'safety' },
    { file: 'compaction-recovery.sh', domain: 'identity' },
    { file: 'external-operation-gate.js', domain: 'safety' },
    { file: 'deferral-detector.js', domain: 'safety' },
    { file: 'post-action-reflection.js', domain: 'evolution' },
    { file: 'external-communication-guard.js', domain: 'safety' },
    { file: 'scope-coherence-collector.js', domain: 'coherence' },
    { file: 'scope-coherence-checkpoint.js', domain: 'coherence' },
    { file: 'free-text-guard.sh', domain: 'safety' },
    { file: 'claim-intercept.js', domain: 'coherence' },
    { file: 'claim-intercept-response.js', domain: 'coherence' },
  ];

  for (const hook of builtinHooks) {
    // Hooks are generated by PostUpdateMigrator at runtime, not from static files.
    // We hash the source method that generates them (PostUpdateMigrator.ts) keyed by hook name.
    // This means the manifest hash changes when the generator code changes.
    const sourceFile = path.join(ROOT, 'src/core/PostUpdateMigrator.ts');
    const sourceHash = fileExists(sourceFile) ? sha256File(sourceFile) : 'generated-at-runtime';

    entries.push({
      id: `hook:${hook.file.replace(/\.(sh|js)$/, '')}`,
      type: 'hook',
      domain: hook.domain,
      sourcePath: `src/core/PostUpdateMigrator.ts`,
      installedPath: `.instar/hooks/instar/${hook.file}`,
      contentHash: sourceHash,
      since: '2025-01-01',
    });
  }

  return entries;
}

/**
 * Scan default jobs from getDefaultJobs() in init.ts.
 * We extract job slugs by parsing the source.
 */
function scanDefaultJobs() {
  const entries = [];
  const initPath = path.join(ROOT, 'src/commands/init.ts');

  if (!fileExists(initPath)) return entries;

  const content = fs.readFileSync(initPath, 'utf-8');

  // Extract job slugs from the getDefaultJobs function
  const slugRegex = /slug:\s*'([^']+)'/g;
  const functionStart = content.indexOf('function getDefaultJobs');
  if (functionStart === -1) return entries;

  // Find the closing of the function (next function or end)
  const functionSection = content.slice(functionStart);
  let match;
  const slugs = [];
  while ((match = slugRegex.exec(functionSection)) !== null) {
    slugs.push(match[1]);
  }

  // Hash the getDefaultJobs function section for drift detection
  const functionEnd = functionSection.indexOf('\n}\n');
  const functionBody = functionSection.slice(0, functionEnd > 0 ? functionEnd + 3 : undefined);
  const functionHash = sha256(functionBody);

  const jobDomainMap = {
    'health-check': 'monitoring',
    'reflection-trigger': 'evolution',
    'relationship-maintenance': 'relationships',
    'update-check': 'updates',
    'feedback-retry': 'feedback',
    'dispatch-check': 'dispatches',
    'self-diagnosis': 'monitoring',
    'evolution-review': 'evolution',
    'insight-harvest': 'evolution',
    'commitment-check': 'commitments',
    'project-map-refresh': 'mapping',
    'coherence-audit': 'coherence',
    'degradation-digest': 'monitoring',
    'state-integrity-check': 'monitoring',
    'memory-hygiene': 'memory',
    'guardian-pulse': 'monitoring',
    'session-continuity-check': 'sessions',
    'memory-export': 'memory',
    'git-sync': 'coordination',
  };

  for (const slug of slugs) {
    entries.push({
      id: `job:${slug}`,
      type: 'job',
      domain: jobDomainMap[slug] || 'general',
      sourcePath: 'src/commands/init.ts',
      contentHash: functionHash,
      since: '2025-01-01',
    });
  }

  return entries;
}

/**
 * Scan shipped skills from .claude/skills/.
 */
function scanSkills() {
  const entries = [];
  const skillsDir = path.join(ROOT, '.claude/skills');

  if (!fs.existsSync(skillsDir)) return entries;

  const skillFolders = fs.readdirSync(skillsDir, { withFileTypes: true })
    .filter(d => d.isDirectory());

  for (const folder of skillFolders) {
    const skillPath = path.join(skillsDir, folder.name, 'skill.md');
    const altPath = path.join(skillsDir, folder.name, 'SKILL.md');
    const actualPath = fileExists(skillPath) ? skillPath : (fileExists(altPath) ? altPath : null);

    if (!actualPath) continue;

    entries.push({
      id: `skill:${folder.name}`,
      type: 'skill',
      domain: 'skills',
      sourcePath: path.relative(ROOT, actualPath),
      contentHash: sha256File(actualPath),
      since: '2025-01-01',
    });
  }

  return entries;
}

