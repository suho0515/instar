/**
 * Scan shipped template files.
 */
function scanTemplates() {
  const entries = [];
  const templateDirs = [
    { dir: 'src/templates/hooks', type: 'template', domain: 'safety' },
    { dir: 'src/templates/scripts', type: 'template', domain: 'operations' },
    { dir: 'playbook-scripts', type: 'playbook-script', domain: 'context' },
  ];

  for (const { dir, type, domain } of templateDirs) {
    const fullDir = path.join(ROOT, dir);
    if (!fs.existsSync(fullDir)) continue;

    const files = fs.readdirSync(fullDir, { withFileTypes: true })
      .filter(f => f.isFile());

    for (const file of files) {
      const filePath = path.join(fullDir, file.name);
      entries.push({
        id: `${type}:${file.name}`,
        type,
        domain,
        sourcePath: path.join(dir, file.name),
        contentHash: sha256File(filePath),
        since: '2025-01-01',
      });
    }
  }

  return entries;
}

